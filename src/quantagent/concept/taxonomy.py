def all_leaves() -> list[Leaf]:
    return TAXONOMY


def board_to_leaves() -> dict[str, list[Leaf]]:
    """Map each real board name -> the leaves it belongs to (DAG, multi-parent)."""
    out: dict[str, list[Leaf]] = {}
    for lf in TAXONOMY:
        for b in lf.boards:
            out.setdefault(b, []).append(lf)
    return out


def classify_board(board: str) -> list[Leaf]:
    return board_to_leaves().get(board, [])


def is_concept(board: str) -> bool:
    return board not in EXCLUDE


def coverage(board_universe: list[str]) -> dict:
    """How many live boards the curated taxonomy maps vs leaves uncovered."""
    mapped = set(board_to_leaves())
    live_concepts = [b for b in board_universe if b not in EXCLUDE]
    in_tax = [b for b in live_concepts if b in mapped]
    unmapped = sorted(set(live_concepts) - mapped)
    return {
        "live_boards": len(board_universe),
        "excluded_noise": len([b for b in board_universe if b in EXCLUDE]),
        "concept_boards": len(live_concepts),
        "mapped_to_taxonomy": len(in_tax),
        "leaves": len(TAXONOMY),
        "industries": sorted({lf.industry for lf in TAXONOMY}),
        "unmapped_concepts": unmapped,
    }
